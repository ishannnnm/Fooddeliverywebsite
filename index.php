    <h4>History</h4>
    <p style="text-align:justify;">
		Genesis Print Stuff and Trading (GPST) is a DTI-registered company where printing of personalized gifts and souvenir items became the introductory line of business. It was registered on the month of July 2012 and started its operation on August 2012 after completion of all required government formalities. This company was registered to cover various line of businesses related to manufacturing of personalized items, rentals and trading.

GPST is a proprietary business of Engr. & Mrs. Domiciano Bejer. The conceptualization and setting-up of this business materialized for a period of less than one month during their vacation from working abroad. The idea of establishing this company is to have a small-scale business which would help relatives to have an extra income and at the same time to get familiar with all the aspects of entrepreneurship. What prompted them to choose this type of business is due to increasing requirements of printed personalized items especially Filipinos are so much into celebration and by nature givers and also Christmas and national elections are coming soon. The couple together with all the key personnel who are mostly relatives attended the technology-transfer training in Venezuela, Manila and proceeded with the acquisition of facilities needed for starting the business. After a short period of actual application of what had been learned in the training, the business started its operation utilizing fresh minds of the member staff. The proprietors have some sort of fulfillments seeing young minds contributing their creative ideas in each design rather than wasting their idle time.

The name Genesis was biblical as it is the first book in the Bible. It was adopted and incorporated to the name of the company since this is the first business venture of the couple who look forward to a successful business operation and more business ventures that the Almighty God would provide.

Because the owners of the company are both Overseas Filipino Workers (OFW) and has a limited access for managing this business, the direct management of the company was entrusted to their sister-in-law, Ms. Agnes Bauan. The proprietors of GPST are monitoring the entire operation of the company abroad and doing major decision on its operation.

GPST is now engaged in printing of personalized items with the following products and services to offer: Tarpaulin, T-Shirt Printing, Personalized Mugs, Personalized Magic Mugs, Personalized Tumblers, Button Pin, Foldable Fan, PVC ID, Key Chain, Bag Tags, Calling Cards, Invitation Cards, Photo Printing, Document Printing, Ring Binding, Photocopy and Lamination. So far, the products of GPST cover a wide range of satisfactions for election campaign, company souvenirs, birthdays, family reunion, class reunion, sports, retreats, etc. Its facility is located in Brgy. Balete, Batangas City and accepting orders for Batangas City area, nearby towns in Batangas and bulk orders for nearby provinces. GPST can be contacted by email through genesis_ps@yahoo.com.ph and a call on (+6343)3005704. 
